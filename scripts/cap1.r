## Wage


### O banco de dados Wage busca entender o impacto da idade e da educação de um empregado
### no seu salário, de através do tempo. 



## SMarket

###  O banco contém as movimentações diárias da S&P 500 entre 2001 e 2005. Tenta predizer 
###  se o índice vai aumentar ou diminuir de acordo com a % das mudanças dos últimos 5 dias.



## NC160

### Nesse banco de expressão genética, 6830 medidas de expressão de genes para cada uma 
### das 64 células cancerígenas. Aqui, há o intuito não de prever um output, mas entender
### os grupos ou clusters que podem causar câncer.

